#Leia dois int (a e b) e imprima: a + b = X | a - b = Y | a * b = Z
a = int(input("Digite o primeiro número inteiro: "))
b = int(input("Digite o segundo número inteiro: ")) 
soma = a + b
subtracao = a - b
multiplicacao = a * b
print(f"{a} + {b} = {soma} | {a} - {b} = {subtracao} | {a} * {b} = {multiplicacao}")